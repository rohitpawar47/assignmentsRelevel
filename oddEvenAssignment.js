// Program to check if a number is odd or even..

function checkOddEvenNumber(num) {
    
    if (num % 2 === 0) {
        console.log(num + " is Even Number");
    } else {
        console.log(num + " is Odd Number");
    }

}
checkOddEvenNumber(2);
checkOddEvenNumber(3);
checkOddEvenNumber(0);
checkOddEvenNumber(1);
checkOddEvenNumber(55);
checkOddEvenNumber(69);
checkOddEvenNumber(90);