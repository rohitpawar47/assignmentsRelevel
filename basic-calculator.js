// Lets make a basic calculator which will take only 2 values..

// Creating functions..
function add(x,y) {
    let result = x + y;
    return result;
}
function sub(x,y) {
    let result = x - y;
    return result;
}
function mul(x,y) {
    let result = x * y;
    return result;
}
function div(x,y) {
    let result = x / y;
    return result; 
}

// Calling the functions..
console.log("Addition of two numbers: " + add(2,2));
console.log("Substraction of two numbers: " + sub(2,2));
console.log("Multiplication of two numbers: " + mul(2,2));
console.log("Division of two numbers: " + div(2,2));


